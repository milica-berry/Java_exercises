package domaci.domaci1;

public class DrugiZadatak {
    public static void main(String[] args) {

        int x = 7;
        int y = 4;

        int sabiranje = x + y;
        System.out.println("Sabiranje: " + sabiranje);

        int oduzimanje = x - y;
        System.out.println("Oduzimanje: " + oduzimanje);

        int deljenje = x / y;
        System.out.println("Deljenje: " + deljenje);

        int mnozenje = x * y;
        System.out.println("Mnozenje: " + mnozenje);

        int kvadratOdX = x * x;
        System.out.println("X na kvadrat je: " + kvadratOdX);
    }
}
